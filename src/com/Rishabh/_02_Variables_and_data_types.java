package com.Rishabh;
public class _02_Variables_and_data_types {

    public static void main(String[] args) {
        int a = 21;  // no need to specify
        byte age = 34;
        short age1 = 56;
        long num = 4344555556L ;  // need to specify by using L literal

        float b = 21.04f;  // to specify it by using f literal at the nd
        double c = 21.045; // default double

        boolean d = true;
        char e = 'a';

        String str = "Rishabh Saini";

        System.out.println(str);

        System.out.println(a);
        System.out.println(age);
        System.out.println(age1);
        System.out.println(num);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);


    }
}
