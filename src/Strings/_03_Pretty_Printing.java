package Strings;

public class _03_Pretty_Printing {
    public static void main(String[] args) {
        float a = 312.45621f;
        System.out.printf("a : %.3f %n",a);

        System.out.println(Math.PI);
        System.out.printf("PI: %.2f %n",Math.PI);

        System.out.printf("Hello my name is %s, i am a %s.","Rishabh","CSE grad");
    }
}
